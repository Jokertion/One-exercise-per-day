# Python 练习实例28
# 题目：有5个人坐在一起，问第五个人多少岁？
# 他说比第4个人大2岁。问第4个人岁数，他说比第3个人大2岁。
# 问第三个人，又说比第2人大两岁。问第2个人，说比第一个人大两岁。
# 最后问第一个人，他说是10岁。请问第五个人多大？

def count_age(n):
    if n == 1:
        return 10
    else:
        c = count_age(n - 1) + 2
        return c


"""
5: c(4) + 2
4:    c(3) + 2
3:      c(2) + 2
2:         c(1)+2
1:           10
"""
print(count_age(5))
